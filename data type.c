//sample a program illustrating each data type//
#include<stdio.h>
int main()
{
    int num;
    char ch;
    float num2;
    double num3;
    num=10;
    ch='A';
    num2=2.233;
    num3=2.883;
    printf("value of num=%d\n",num);
    printf("value of ch=%c\n",ch);
    printf("value of num2=%f\n",num2);
    printf("%value of num3=%lf\n",num3);
    return 0;
}
